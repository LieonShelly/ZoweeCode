#import "ZEAPPModel.h"

@implementation ZEAPPModel

- (instancetype)initWithItems:(NSArray *)items sectionHeaderTitle:(NSString *)sectionHeaderTitle sectionFooterTitle:(NSString *)sectionFooterTitle
{
    if (self = [super init]) {
        self.items = items;
        self.sectionFooterTitle = sectionFooterTitle;
        self.sectionHeaderTitle = sectionHeaderTitle;
    }
    return self;
}
@end
