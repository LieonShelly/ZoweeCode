#import "workZoneModel.h"
#import "NSDate+string.h"

@implementation workZoneModel

- (NSString *)timeAndTagStr {
    if (_PublishTime && _PublishTag) {
        NSString *timeAndTag = [NSDate stringFormateWithString:_PublishTime];
        timeAndTag = [NSString stringWithFormat:@"%@来自%@",timeAndTag,_PublishTag];
        return timeAndTag;
    }
    return @"";
}

@end


